// V59: OpenGL ES 2.0 pipeline state
struct GLES2State {
    // Viewport
    uint32_t viewportX, viewportY;
    uint32_t viewportWidth, viewportHeight;
    
    // Scissor
    uint32_t scissorX, scissorY;
    uint32_t scissorWidth, scissorHeight;
    bool scissorEnabled;
    
    // Depth/Stencil
    bool depthTestEnabled;
    bool depthWriteEnabled;
    uint32_t depthFunc;
    bool stencilTestEnabled;
    uint32_t stencilFunc;
    uint32_t stencilRef;
    uint32_t stencilMask;
    
    // Blending
    bool blendEnabled;
    uint32_t blendSrcFactor;
    uint32_t blendDstFactor;
    uint32_t blendEquation;
    
    // Clear values
    uint32_t clearColorR, clearColorG, clearColorB, clearColorA;
    float clearDepth;
    uint32_t clearStencil;
    
    // Render target
    uint64_t framebufferAddr;
    uint32_t framebufferWidth, framebufferHeight;
    uint32_t framebufferFormat;
};

// V59: Vertex format
